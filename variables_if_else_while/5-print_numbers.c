#include <stdlib.h>
#include <stdio.h>
#include <time.h>
/**
 * main - Print all single digit number of base 10 starting from 0
 * Description: Printing all single digit number base 10 beginnings from 0
 * Return: 0
 */
int main(void)
{
	int a;

	for (a = 0; a <= 10; a++)
	{
		putchar(0);
	}
	putchar('\n');

	return (0);
}
